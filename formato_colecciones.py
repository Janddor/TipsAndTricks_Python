# Tip. dar formato consistente a dccionarios,listas, o cualquier otra colecci√≥jn

# Def de lista en una sola linea
mi_lista = ['Juan', 'Laura', 'Guadalupe']

# Dar formato a nuestra lista
mi_lista = [
    'Juan',
    'Laura',
    'Guadalupe'
]

# Dar formato pero con posibles errores

mi_lista = [
    'Juan',
    'Laura',
    'Guadalupe'
    'Pedro'
]

# Formato consistente de colecciones implica agregar una comma al final

mi_lista = [
    'Juan',
    'Laura',
    'Guadalupe',
    'Pedro', # La comma del ultimo elemento no importa de esta manera
]

print(mi_lista)